create database QLHS;
go

use QLHS

create table school(
	maTruong varchar(5) primary key not null, 
	tenTruong nvarchar(50)
);

create table schoolYear(
	nienKhoa varchar(9),
	khoi int,
	tenlop varchar(5),
	hocKy int
);

create table teacher(
	MSGV varchar(10) primary key not null,
	tuoi date,
	diaChi nvarchar(100),
	hoTen nvarchar(50),
	email varchar(50),
	sdt varchar(10)
);

create table student(
	MSHS varchar(10) primary key not null,
	tuoi date,
	diaChi nvarchar(100),
	hoTen nvarchar(50),
	email varchar(50),
	sdt varchar(10),
	tenlop varchar(5),
	foreign key (tenlop) references schoolYear(tenlop),
	khoi int,
	foreign key (khoi) references schoolYear(khoi)
);

create table parents(
	MSHS varchar(10),
	foreign key (MSHS) references student(MSHS),
	hoTen nvarchar(50),
	sdt varchar(10)
);

create table science(
	maKhoa varchar(5) primary key not null,
	tenKhoa nvarchar(20)
);

create table class(
	maLop varchar(10) primary key not null,
	tenLop varchar(5),
	foreign key (tenLop) references schoolYear(tenLop)
);

create table subject(
	maMH varchar(10) primary key not null,
	tenMH nvarchar(50)
);

create table grade(
	maMH varchar(10),
	foreign key (maMH) references subject(maMH),
	MSHS varchar(10),
	foreign key (MSHS) references student(MSHS),
	diemSo float
);

