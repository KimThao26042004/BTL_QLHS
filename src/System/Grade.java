package System;

public class Grade {
	private String maMH;
	private String MSHS;
	private float diemSo;
	
	
	public Grade(String maMH, String mSHS, float diemSo) {
		super();
		this.maMH = maMH;
		MSHS = mSHS;
		this.diemSo = diemSo;
	}


	public Grade() {
		super();
		this.maMH = "";
		MSHS = "";
		this.diemSo = 0;
	}
	
	
	
}
