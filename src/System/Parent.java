package System;

public class Parent {
	private String MSHS;
	private String ten_PH;
	private String SDT_PH;
	
	
	public Parent(String mSHS, String ten_PH, String sDT_PH) {
		super();
		MSHS = mSHS;
		this.ten_PH = ten_PH;
		SDT_PH = sDT_PH;
	}


	public Parent() {
		super();
		MSHS = "";
		this.ten_PH = "";
		SDT_PH = "";
	}
	
	
	
}
